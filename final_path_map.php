  <script>
    window.onload = function() {
      var urlParams = new URLSearchParams(window.location.search);
      var encodedResult = urlParams.get('result');
      var pathArray = JSON.parse(decodeURIComponent(encodedResult));
    
      var buttons = document.getElementsByClassName('button');
    
      function resetColors() {
        for (var i = 0; i < buttons.length; i++) {
          buttons[i].className = 'btn initial';
        }
      }
    
      function highlightPath(path, colorClass) {
        for (var i = 0; i < path.length; i++) {
          var button = document.getElementById(path[i]);
          button.className = 'btn ' + colorClass;
        }
      }
    
      // Reset colors and highlight the paths
      resetColors();
      highlightPath(pathArray.first_node, 'start');
      for (var i = 0; i < pathArray.last_node.length; i++) {
        highlightPath([pathArray.last_node[i]], 'end');
      }
      for (var i = 0; i < pathArray.intermediate_array.length; i++) {
        highlightPath([pathArray.intermediate_array[i]], 'path');
      }
    };
  </script>
  
  
  
